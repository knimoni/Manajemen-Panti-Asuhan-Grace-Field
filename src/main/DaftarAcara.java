    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here:
        namaacara.setText(namaacara.getText());
        log.setNama(namaacara.getText());
        String Tanggall = ((JTextField)tanggal.getDateEditor().getUiComponent()).getText();
        log.setTanggall(Tanggall);
        keterangan.setText(keterangan.getText());
        log.setKeterangan(keterangan.getText());
        if (namaacara.getText().trim().isEmpty() || Tanggall.trim().isEmpty() || keterangan.getText().trim().isEmpty()){
                 JOptionPane.showMessageDialog(null, "ISI DENGAN LENGKAP", "WARNING", HEIGHT);
        }
        else {
            JOptionPane.showMessageDialog(null, "data berhasil ditambahkan", "SUCCESSFULLY", HEIGHT);
            DefaultTableModel model = (DefaultTableModel) daftar.getModel();
            model.addRow(new Object[]{log.getNama(), log.getTanggall(), log.getKeterangan()});
        }
    }//GEN-LAST:event_addActionPerformed

