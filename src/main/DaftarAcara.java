    private void daftarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_daftarMouseClicked
        // TODO add your handling code here:
        int index = daftar.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel)daftar.getModel();

        String Nama = model.getValueAt(index, 0).toString();
        log.setNama(Nama);
        String Tanggall = model.getValueAt(index, 1).toString();
        log.setTanggall(Tanggall);
        String Keterangan = model.getValueAt(index, 2).toString();
        log.setKeterangan(Keterangan);
        lihat.setVisible(true);
        lihat.pack();
        lihat.setLocationRelativeTo(null);
        lihat.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);

        lihat.Nama.setText(log.getNama());
        lihat.Tanggall.setText(log.getTanggall());
        lihat.Keterangan.setText(log.getKeterangan());
    }//GEN-LAST:event_daftarMouseClicked

